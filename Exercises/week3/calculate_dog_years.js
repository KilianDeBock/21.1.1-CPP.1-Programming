function calculateDogYears(humanYears) {
  return humanYears * 7;
}

let doggyYears = calculateDogYears(1);
console.log(`Your dog is ${doggyYears} years in dog years!`);
doggyYears = calculateDogYears(13);
console.log(`Your dog is ${doggyYears} years in dog years!`);
doggyYears = calculateDogYears(4);
console.log(`Your dog is ${doggyYears} years in dog years!`);
